public class Main {
    public static void main(String[] args) {
        double euros = 100.0; // Puedes cambiar este valor según la cantidad de euros que desees convertir
        double factorConversion = 166.386; // Factor de conversión de euros a pesetas
        double pesetas = euros * factorConversion;

        System.out.println("Cantidad en euros: " + euros);
        System.out.println("Cantidad en pesetas: " + pesetas);
    }
}
